# Operadores

print(3 + 4)      # Suma
print(3 - 4)      # Resta
print(3 / 4)      # División  
print(3 * 4)      # Multiplicación
print(10 % 2)     # Resto
print(10 // 3)    # Flor división
print(2 ** 3)     # Exponente
print(2 ** 3 + 3 - 7 / 1 // 4 )

print("Hola" + " Python") # Concatenación
print("Hola" + str(5))
print("Hola" * 5) 
print("Hola" * (2**3))

my_float = 2.5
print("Hola" * int(my_float))

# Operadores comparativos

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)


print("Hola" > "Python")
print("Hola" < "Python")
print("aaaa" >= "abaa")     # Ordenación alfabética
print(len('aaaa') <= len('abaa'))   # Cuenta caracteres
print("Hola" == "Python")
print("Hola" != "Python")


# Operadores lógicos

print(3 > 4 and "Hola" > "Python")
print(3 > 4 or "Hola" > "Python")
print(3 < 4 and "Hola" < "Python")
print(3 < 4 or "Hola" > "Python")
print(not 3 > 4)
